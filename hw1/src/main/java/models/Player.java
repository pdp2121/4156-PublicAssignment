package models;

public class Player {

  private char type;

  private int id;

  /**
   * Getter for type.
   * @return the type
   */
  public char getType() {
    return type;
  }

  /**
   * Setter for type.
   * @param type the type to set
   */
  public void setType(char type) {
    this.type = type;
  }

  /**
   * Getter for id.
   * @return the id
   */
  public int getId() {
    return id;
  }

  /**
   * Setter for id.
   * @param id the id to set
   */
  public void setId(int id) {
    this.id = id;
  }

}
